// Data class for dates. 
// Stores person name, date of birth, date of current date(include day, month, year)

public class dates {
	String sName ="";
	String sDayBirth;
	String sMonthBirth,sYearBirth ;
	String sDayCurrent,sMonthCurrent,sYearCurrent;
	
	public dates(String sName,String sDayBirth,String sMonthBirth,String sYearBirth,String sDayCurrent,String sMonthCurrent, String sYearCurrent )
	{
		this.sName = sName;
		this.sDayBirth = sDayBirth;
		this.sMonthBirth = sMonthBirth;
		this.sYearBirth = sYearBirth;
		this.sDayCurrent = sDayCurrent;
		this.sMonthCurrent = sMonthCurrent;
		this.sYearCurrent = sYearCurrent;
		
	}
		
	}
	


